let change_cell=[];


function check(a,b)
{
  if(a<30 && b<30 && a>-1 && b>-1)
  {
    return true;
  }
  return false;
}


function setup() {
    createCanvas(900,900);
    background(255);
    frameRate(10);
}



function checkCell(i,j)
{
  let count=0;
  
  if(check(i-1,j-1)  && maze_arr[i-1][j-1]==1)
    count++;
  
  if(check(i-1,j) && maze_arr[i-1][j]==1)
    count++;
  
  if(check(i,j-1) && maze_arr[i][j-1]==1)
    count++;
  
  if(check(i+1,j) && maze_arr[i+1][j]==1)
    count++;
  
  if(check(i,j+1) && maze_arr[i][j+1]==1)
    count++;
  
  if(check(i+1,j+1) && maze_arr[i+1][j+1]==1)
    count++;
  
  if(check(i+1,j-1) && maze_arr[i+1][j-1]==1)
    count++;
  
  if(check(i-1,j+1) && maze_arr[i-1][j+1]==1)
    count++;
  
  if(count<2)
    change_cell.push([i,j,0]);
  
  else if(count==3)
    change_cell.push([i,j,1]);
  
  else if(count>3)
    change_cell.push([i,j,0]);
}



function draw() {
  
  
  let x=0
  
  for(let i=0;i<maze_arr.length;i++)
  {
    for(let j=0;j<maze_arr[i].length;j++)
    {
      
      checkCell(i,j);
    }
  }
  
  
  while(change_cell.length)
  {
    const [i,j,val]=change_cell.pop();
    
    maze_arr[i][j]=val;
    
    if(maze_arr[i][j])
    {
      fill(0);
      square(x+int(j*30),x+int(i*30),30);
    }
    else
    {
      fill(255);
      square(x+int(j*30),x+int(i*30),30);
    }
    
  }
  
}
